package tub.ods.mediator.model;

public class ConfigValueRequest {
	
	long price;
	long limit;
	
	public long getPrice() {
		return price;
	}
	public void setPrice(long price) {
		this.price = price;
	}
	public long getLimit() {
		return limit;
	}
	public void setLimit(long limit) {
		this.limit = limit;
	}
	
	

}
